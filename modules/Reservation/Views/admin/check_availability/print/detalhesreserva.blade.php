            if (expand) {
                $.each(expandButton, function () {
                    if ($(this).find("i").hasClass("fa-caret-right")) {
                        $(this).click();
                    }
                });
                $(this).find("i").removeClass("fa-caret-right").addClass("fa-caret-down");
